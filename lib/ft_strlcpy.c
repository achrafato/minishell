#include "../minishell.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	t_srclen;
	size_t	i;

	i = 0;
	t_srclen = ft_strlen(src);
	if (dstsize < 1)
		return (t_srclen);
	while (src[i] != '\0' && i < (dstsize - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (t_srclen);
}
